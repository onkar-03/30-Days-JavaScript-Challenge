// Arithmetic Operations Script:
// - Write a script that performs basic arithmetic operations (addition, subtraction, multiplication, division, remainder) on two numbers and logs the results

// Defined two numbers
const num1 = 15;
const num2 = 4;

// Perform arithmetic operations
const addition = num1 + num2;
const subtraction = num1 - num2;
const multiplication = num1 * num2;
const division = num1 / num2;
const remainder = num1 % num2;

// Log the results to the console
console.log(`Arithmetic Operations on ${num1} and ${num2}:`);
console.log(`Addition: ${num1} + ${num2} = ${addition}`);
console.log(`Subtraction: ${num1} - ${num2} = ${subtraction}`);
console.log(`Multiplication: ${num1} * ${num2} = ${multiplication}`);
console.log(`Division: ${num1} / ${num2} = ${division}`);
console.log(`Remainder: ${num1} % ${num2} = ${remainder}`);
