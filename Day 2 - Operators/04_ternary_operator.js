// Ternary Operator Script:
// - Write a script that uses the ternary operator to determine if a number is positive or negative and logs the result

// Define a number
const number = -7;

// Use the ternary operator to determine if the number is positive, negative, or zero
const result = number > 0 ? 'Positive' : number < 0 ? 'Negative' : 'Zero';

// Log the result to the console
console.log(`The number ${number} is ${result}.`);
